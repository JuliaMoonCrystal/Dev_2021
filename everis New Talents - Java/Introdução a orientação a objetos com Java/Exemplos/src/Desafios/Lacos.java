public class Lacos {
    public static void main(String[] args) {
 /*While*/
                int contador = 0;

                while(contador <= 10) {
                    System.out.println(contador);
                    contador = contador + 1;
                }
/*For*/
        for(int cont = 0; cont <= 10; cont++) {
            System.out.println(cont);
        }
    }
}
